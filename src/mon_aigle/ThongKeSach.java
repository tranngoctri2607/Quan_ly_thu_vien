    void tongSoSach() throws SQLException {
        String sqlServer = "localhost";
        String port = "1433";
        String user = "sa";
        String passWord = "sa";
        String database = "QuanLyThuVien";
        String url = "jdbc:sqlserver://" + sqlServer + ":" + port + ";user=" + user + ";password=" + passWord + ";databaseName=" + database + ";encrypt=false";
        // 2. Open connection
        try (Connection con = DriverManager.getConnection(url)) {
            // 3. Create Statement
            Statement st = con.createStatement();
            String sqlInsert = "SELECT COUNT(IDsach) 'TongSo' FROM QuanLySach;";
            ResultSet rs = st.executeQuery(sqlInsert);
            while (rs.next()) {
                String x = rs.getString("TongSo");
                lblTongSach.setText("Tổng sách: " + x);
            }
            rs.close();
            st.close();
            // 5. Close connection
            con.close();
        } catch (SQLServerException e) {
            System.out.println(e);
        }
    }

