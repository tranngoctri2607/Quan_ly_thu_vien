    private void loadData1(String input) throws SQLException {
        try {
            Statement st = con.createStatement();
            rs = st.executeQuery("select IDsach 'Mã sách', count(IDsach) 'Số lượng' from QuanLyMuonTra where month(NgayMuon) = " + input + " group by IDsach");
            data.removeAllElements();
            if (!fill) {
//get header
                ResultSetMetaData rsmd = rs.getMetaData();
                int n = rsmd.getColumnCount();
                col.add(rsmd.getColumnName(1));
                col.add(rsmd.getColumnName(2));
            }
//get data
            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString(1));
                v.add(rs.getString(2));
                data.add(v);
            }
            tbl = new DefaultTableModel(data, col);
            this.tblSachMuon.setModel(tbl);
            rs.close();
            st.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }
    }

