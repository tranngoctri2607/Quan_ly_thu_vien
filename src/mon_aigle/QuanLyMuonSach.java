    void themDuLieu() throws SQLException {
        // 2. Open connection
        try (Connection con = DriverManager.getConnection(url)) {
            String ngayMuon = txtNgayMuon.getText();
            String ngayTra = txtNgayTra.getText();
            String idSinhVien = txtIDSV.getText();
            String idSach = txtIDSach.getText();
            // 3. Create Statement
            Statement st = con.createStatement();
            // 4. Execute query
            String sqlInsert = "INSERT INTO QuanLyMuonTra(IDsach, IDsinhvien, NgayMuon, NgayTra) VALUES ('" + idSach + "', '" + idSinhVien + "', '" + ngayMuon + "', '" + ngayTra + "')";
            ResultSet rs = st.executeQuery(sqlInsert);
            rs.close();
            st.close();
            // 5. Close connection
            con.close();
        } catch (SQLServerException e) {
            System.out.println(e);
        }
    }

    void batLoiDate() throws ParseException, SQLException {
        try {
            String muon = txtNgayMuon.getText();
            DateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
            Date ngayMuon = dateFormat.parse(muon);
            txtNgayMuon.setText(String.valueOf(new SimpleDateFormat("yyyy-MM-dd").format(ngayMuon)));
            String tra = txtNgayTra.getText();
            Date ngayTra = dateFormat.parse(tra);
            if (ngayMuon.before(ngayTra)) {
                txtNgayTra.setText(String.valueOf(new SimpleDateFormat("yyyy-MM-dd").format(ngayTra)));
                themDuLieu();
            } else {
                JOptionPane.showMessageDialog(this, "Ngày trả phải sau ngày mượn");
            }
        } catch (ParseException parseException) {
            JOptionPane.showMessageDialog(this, "Sai định dạng");
        }
    }

