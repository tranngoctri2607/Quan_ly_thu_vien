    void hienThiTenSach(String ID) throws SQLException {
        // 2. Open connection
        try (Connection con = DriverManager.getConnection(url)) {
            // 3. Create Statement
            Statement st = con.createStatement();
            // 4. Execute query
            String sqlInsert = "SELECT TenSach FROM QuanLySach WHERE IDsach='" + ID + "';";
            ResultSet rs = st.executeQuery(sqlInsert);
            while (rs.next()) {
                String tenSach = rs.getString("TenSach");
                txtTen.setText(tenSach);
                // print the results
            }
            rs.close();
            st.close();
            // 5. Close connection
            con.close();
        } catch (SQLServerException e) {
            System.out.println(e);
        }
    }

